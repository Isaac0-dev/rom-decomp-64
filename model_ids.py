"""
Model ID lookup tables, based on sm64 decomp's model_ids.h.

Uses two primary data structures:
  1. MODEL_ID_BY_VALUE: dict[int, list[str]]
     Maps each numeric value to ALL constant names that share that value.
     e.g. 0x55 -> ["MODEL_YELLOW_SPHERE", "MODEL_WATER_BOMB_SHADOW", "MODEL_POKEY_BODY_PART", ...]

  2. MODEL_ID_BY_LEVEL: dict[str, dict[int, str]]
     Maps a level short-name (e.g. "bbh", "ccm") to a dict of {value: constant_name}
     for model IDs that are specific to that level.

The main entry point is `resolve_model_id(value, level_name)` which returns the best
constant name (or a comment listing candidates) for a given numeric model ID.

Future work:
  - Cross-reference with geo layout / display list data loaded by LOAD_MODEL_FROM_GEO
    and LOAD_MODEL_FROM_DL to further disambiguate which specific constant is correct.
  - Use the resolved model ID to improve vanilla_matcher accuracy.
"""


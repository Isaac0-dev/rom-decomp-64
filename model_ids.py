def resolve_model_id(value: int, level_name: Optional[str] = None) -> Optional[str]:
    """
    Resolve a numeric model ID to its constant name.

    Args:
        value: The numeric model ID (e.g. 0x55)
        level_name: The current level short-name (e.g. "bbh", "ssl").
                    Used to disambiguate level-specific model IDs.

    Returns:
        A string for the model constant, or None if no match.
        For unique matches: "MODEL_GOOMBA"
        For level-disambiguated matches: "MODEL_BBH_STAIRCASE_STEP"
        For ambiguous matches (multiple candidates, no level context):
            "MODEL_YELLOW_SPHERE /* or MODEL_WATER_BOMB_SHADOW, ... */"

    Future: This function could accept additional context (geo layout pointer,
    display list pointer) to further disambiguate model IDs, especially for
    the actor group bins (0x54-0x63, 0x64-0x73) where the loaded actor group
    determines which constant is correct.
    """
    candidates = MODEL_ID_BY_VALUE.get(value)
    if not candidates:
        return None

    # Unique match — easy case
    if len(candidates) == 1:
        return candidates[0]

    # Try level-specific disambiguation
    if level_name:
        level_models = _MODEL_ID_BY_LEVEL.get(level_name, {})
        level_match = level_models.get(value)
        if level_match:
            return level_match

    # Filter to only non-level-specific candidates (global models)
    # These are models that don't have a level prefix pattern
    # For now, just return all candidates as a comment
    # Future: use geo/dl context to narrow down further

    primary = candidates[0]
    if len(candidates) <= 3:
        others = ", ".join(candidates[1:])
        return f"{primary} /* or {others} */"
    else:
        others = ", ".join(candidates[1:3])
        return f"{primary} /* or {others}, ... ({len(candidates)} total) */"
